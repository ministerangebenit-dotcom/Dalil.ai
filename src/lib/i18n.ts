export function t(lang: Language, key: keyof typeof translations['en']): any {
  return translations[lang][key] ?? translations['en'][key];
}

export function getGreeting(lang: Language): string {
  const hour = new Date().getHours();
  if (hour < 12) return t(lang, 'greeting_morning');
  if (hour < 18) return t(lang, 'greeting_afternoon');
  return t(lang, 'greeting_evening');
}
